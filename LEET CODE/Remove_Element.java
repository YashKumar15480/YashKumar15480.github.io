class Solution1221 {
    public int removeElement(int[] nums, int val) {
        int size = nums.length;
        int k = 0;
        for (int i = 0; i < nums.length; i++) {
            if (nums[i] != val) {
                nums[k] = nums[i];
                k++;
            }
        }
        return k;
    }
}

